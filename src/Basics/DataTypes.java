package Basics;

public class DataTypes {
    public static void main(String[] args) {
        int rollNo = 43;
        char myChar = '$';
        float myGradingPoint = 6.33f;
        double largeDecimalNumbers = 732812346.65;
        long largeInteger = 672316798L;
        boolean checkCondition = true;
        System.out.println(rollNo);
        System.out.println(myChar);
        System.out.println(myGradingPoint);
        System.out.println(largeDecimalNumbers);
        System.out.println(largeInteger);
        System.out.println(checkCondition);
        int money = 1_20_000;
        System.out.println(money);
    }
}
