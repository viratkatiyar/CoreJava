package Basics;

public class PatternUsingSinglePrintStatement {
    public static void main(String[] args) {
        //Pattern 1 - Right Half Pyramid
        System.out.println("*\n* *\n* * *\n* * * *\n* * * * *");

        System.out.println();

        //Pattern 2 - Reverse Right Half Pyramid
        System.out.println("* * * * *\n* * * *\n* * *\n* *\n*");

        //Pattern 3 -left Half Pyramid
        System.out.println("        *\n      * *\n    * * *\n  * * * *\n* * * * *");

    }
}
